          <br>-Con la provincialización, el legendario Industrial pasó a llamarse Escuela de Educación Técnica Nº3 Domingo Faustino Sarmiento y, finalmente desde el 1 de marzo de 2010 se le da el nombre de Escuela de Educación Secundaria Técnica Nº3 Domingo Faustino Sarmiento.
