        <p>-En el mes de mayo de 1936 el diario El Progeso anunciaba:
          La municipalidad de Mar del Plata espera inaugurar el próximo 25 de mayo la Escuela de Artes y Oficios. Las obras del nuevo edificio se están levantando en el lugar que anteriormente ocupaba la Administración de Limpieza, en las calles 14 de Julio y Gascón.
          <br>-En mayo de ese mismo año, la empresa Casino Pueyrredon en nota dirigida al intendente, D. José Camusso, había propuesto donar un lote de maquinarias con destino a la Sección Mecánica, además solicitaba autorización para iniciar la construcción de seis aulas en terrenos municipales.
          .</p>
