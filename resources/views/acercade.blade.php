        <p>-La escuela muestra su evolución en el aumento de la matrícula y docentes, en las distintas especialidades que se dictaron y se dictan y en los cambios de denominación a partir de su nacionalización y posterior provincialización.
          <br>-Así, por decreto Nº37179/44 del Presidente Farrel pasa a depender del Ministerio de Justicia e Instrucción Pública con el nombre de Escuela Nacional de Artes y Oficios entre el 30 de diciembre de 1944 al 28 de febrero de 1945.
          <br>-Con el decreto Nº6428/45 fue elevada a la categoría de Escuela Técnica hasta el 18 de abril de 1949. Al agregarse el Ciclo Técnico, pasa a denominarse Escuela Industrial de La Nación Ciclo Superior de Mar del Plata, desde el 19 de abril de 1949 hasta el 12 de junio de 1961, por resolución ministerial.
          .</p>
